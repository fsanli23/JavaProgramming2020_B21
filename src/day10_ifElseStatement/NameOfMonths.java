package day10_ifElseStatement;

public class NameOfMonths {
    public static void main(String[] args) {
        int number=5;
        String month;
        if (number==1){
            month="january";
        }else if (number==2){
            month="february";
        }else if (number==3){
            month="march";
        }else if (number==4){
            month="april";
        }else if (number==5) {
            month = "may";
        }else if (number==6){
            month="june";
        }else if (number==7){
            month="july";
        }else if (number==8){
            month="august";
        }else if (number==9) {
            month = "sept";
        }else if (number==10){
            month="oct";
        }else if (number==11){
            month="nov";
        }else {
            month="dec";
        }
        System.out.println(month);






    }
}
