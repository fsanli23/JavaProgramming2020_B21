package day03_Variables;

public class EscapeSequences {
    public static void main(String[] args) {

        System.out.println("hello Everyone\nmy name is fatih sanli\ni am from texas");

        System.out.println("====================================");
        System.out.println("\ttoday  i learned java");
        System.out.println("=================================");
        System.out.println("\\");
        System.out.println("==============================");
        System.out.println("my name is 'fatih'");
        System.out.println("my name is \'fatih\'");
        System.out.println("============================");
        System.out.println("my favorite tv show is \"breaking bad\"");






    }
}
